/* This is the total count of single tests completed successfuly. */
u32 total_single_successful_test_count = 0;

/* This function runs all of the unit tests and returns the percent success. */
percent tester_run_all_tests()
{
    START_PROFILING("run tests", NULLPTR);

    printf("[  0.00%%] Running tests...\n");
    TEST_INIT();

    TESTER_DECLARE(tester_run_libc_tests());

    printf("[%.2f%%] Tests completed\n", _percent);
    printf("%u / %u \t (Successful / Total)\n", \
    total_single_successful_test_count, total_single_test_count);

    END_PROFILING("run tests", false);
    return TEST_RESULT();
}

