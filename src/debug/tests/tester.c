/*
 * This hashes the inputted u32. This is used to produce sudo random numbers
 * during unit tests. This has an entropy level of about 0.973536.
 */
u32 hash_u32(u32 _u32)
{
    u32 result = UINT32_MAX;
    for (u32 i=0; i < 4; i++) {
        result ^= hash_table_values[((u8*)&_u32)[i]];
        result = (result << 6) + (result >> 28);
        result *= 17;
    }

    return result;
}